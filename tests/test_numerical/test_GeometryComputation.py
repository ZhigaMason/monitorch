def test_geometry_computation(tensors, normalize):
    eps = 1e-8
    gc_inmemory = GeometryComputation(dot_product=True, inplace=False, normalize=normalize, eps=eps)
    gc_inplace = GeometryComputation(dot_product=True, inplace=True, normalize=normalize, eps=eps)
    norm_list = []
    prod_list = []
    prev_tensor = 0
    prev_norm = 0
    for t in tensors:
        gc_inmemory.update(t)
        gc_inplace.update(t)

        norm = torch.linalg.vector_norm(t)
        prod = torch.sum(t * prev_tensor) / (norm * prev_norm + eps)

        prev_norm = copy.deepcopy(norm)
        prev_tensor = copy.deepcopy(t)
        if normalize:
            norm /= np.sqrt(t.numel())
        norm_list.append(norm)
        prod_list.append(prod)

    rmv_norm, rmv_prod = gc_inplace.value
    assert np.allclose(
        [np.mean(norm_list), np.var(norm_list), np.min(norm_list), np.max(norm_list)],
        [rmv_norm.mean, rmv_norm.var, rmv_norm.min_, rmv_norm.max_],
    )
    assert np.allclose(
        [np.mean(prod_list), np.var(prod_list), np.min(prod_list), np.max(prod_list)],
        [rmv_prod.mean, rmv_prod.var, rmv_prod.min_, rmv_prod.max_],
    )

    gc_norm, gc_prod = gc_inmemory.value
    assert np.allclose(norm_list, gc_norm)
    assert np.allclose(prod_list, gc_prod)


def test_geometry_computation_interface():
    gc_norm = GeometryComputation(dot_product=False, inplace=True, eps=0, normalize=True)
    gc_prod = GeometryComputation(dot_product=True, inplace=True, eps=0, normalize=True)
    t = torch.Tensor([1.0, 1.0, 1.0])
    gc_norm.update(t)
    gc_norm.update(t)
    gc_prod.update(t)
    gc_prod.update(t)
    gc_norm.update(t)
    norm = gc_norm.value
    pnorm, _ = gc_prod.value
    assert np.isclose(norm.mean, pnorm.mean)
